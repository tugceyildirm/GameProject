package Objeler;

public class Yol {
	private int xKord, yKord;

	public Yol(int xKord, int yKord) {
		this.xKord = xKord;
		this.yKord = yKord;
	}

	public int getxKord() {
		return xKord;
	}

	public void setxKord(int xKord) {
		this.xKord = xKord;
	}

	public int getyKord() {
		return yKord;
	}

	public void setyKord(int yKord) {
		this.yKord = yKord;
	}

}
